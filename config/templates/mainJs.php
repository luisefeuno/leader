    // Función para formatear la fecha en formato europeo (día/mes/año) con hora
    function formatearFechaEuropea(fecha) {
        let date = new Date(fecha.replace(' ', 'T')); // Reemplazar espacio con 'T' para formato ISO
        let dia = date.getDate();
        let mes = date.getMonth() + 1; // Los meses en JavaScript son 0-11
        let año = date.getFullYear();
        let horas = date.getHours();
        let minutos = date.getMinutes();
        let segundos = date.getSeconds();

        // Agregar ceros a día, mes, horas, minutos y segundos si son menores de 10
        if (dia < 10) dia = '0' + dia;
        if (mes < 10) mes = '0' + mes;
        if (horas < 10) horas = '0' + horas;
        if (minutos < 10) minutos = '0' + minutos;
        if (segundos < 10) segundos = '0' + segundos;

        return dia + '-' + mes + '-' + año + ' ' + horas + ':' + minutos + ':' + segundos;

    }



